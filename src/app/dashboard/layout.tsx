import type { Metadata } from "next";
import { Inter } from "next/font/google";
import Sidenav from "@/src/components/navbar/sidenav";
import { IMember, INavOption } from "@/src/lib/definitions";
import {
  ArrowLeftRight,
  Book,
  BookCheck,
  BookPlus,
  CalendarCheck,
  PersonStanding,
} from "lucide-react";
import { getUserSession } from "@/src/lib/actions";
import { Toaster } from "@/src/components/ui/toaster";
import { redirect } from "next/navigation";
import { User } from "next-auth";
import { getTranslations } from "next-intl/server";

const inter = Inter({ subsets: ["latin"] });

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const t = await getTranslations("Sidenav");

  const navOptions: INavOption[] = [
    { label: `${t("books")}`, url: "/dashboard", iconName: "Book" },
    {
      label: `${t("myBooks")}`,
      url: "/dashboard/myBooks",
      iconName: "BookCheck",
    },
    {
      label: `${t("myRequests")}`,
      url: "/dashboard/myRequests",
      iconName: "BookPlus",
    },
    {
      label: `${t("professors")}`,
      url: "/dashboard/professors",
      iconName: "PersonStanding",
    },
    {
      label: `${t("myAppointments")}`,
      url: "/dashboard/myAppointments",
      iconName: "CalendarCheck",
    },
  ];
  const user = await getUserSession();
  if (!user) {
    redirect("/login");
  }

  return (
    <div className="flex  flex-1 flex-col h-screen lg:flex-row">
      <Sidenav user={user} navOptions={navOptions} />
      <div className="flex flex-col flex-1 overflow-y-auto">
        {children}
        <Toaster />
      </div>
    </div>
  );
}
